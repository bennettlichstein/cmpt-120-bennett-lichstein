def add(a, b):
   # add a and b
    return a + b


def subtract(a, b):
    # subract a and b
    return a - b


def multiply(a, b):
    # multiply a and b
    return a * b


def divide(a, b):
   # divide a and b
    return a / b


def power(a, b):
    # a to the power of b
    return pow(a, b)


def mod(a, b):
   # remainder of a and b
    return a % b
